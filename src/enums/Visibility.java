package enums;

/**
 * Represents the visibility status for project in the BTO management system.
 * This enum defines whether projects should be displayed
 * or hidden to the user when user views the project list.
 */
public enum Visibility {
    /**
     * Indicates that the project should be visible.
     */
    ON,

    /**
     * Indicates that the project should be hidden.
     */
    OFF,
}