package enums;

/**
 * Represents the available flat types in the BTO housing system.
 * This enum defines the different flat configurations that applicants
 * can apply for in BTO projects.
 */
public enum FlatType {
    /**
     * Represents a two-room flat configuration.
     */
    TWO_ROOMS,

    /**
     * Represents a three-room flat configuration.
     */
    THREE_ROOMS,
}