package enums;

/**
 * Represents the marital status options for applicants in the BTO housing system.
 * This enum defines the different marital statuses that can affect an applicant's
 * eligibility and priority for BTO flat applications.
 */
public enum MaritalStatus {
    /**
     * Represents an unmarried applicant.
     */
    SINGLE,

    /**
     * Represents a legally married applicant.
     */
    MARRIED
}